public class Sample9 {
    public static void main(String[] args) {
        int num1, num2;  // 宣告變數
        num1 = 3;        // 指定 num1 的值
        System.out.println("變數 num1 的值是: " + num1);

        num2 = num1;     // 將 num1 的值指定給 num2
        System.out.println("將變數 num1 的值指定到變數 num2 之中");
        System.out.println("變數 num2 的值是: " + num2);
    }
}
