public class Sample11 {
    public static void main(String[] args) {
        System.out.println("1 + 2 等於 " + (1 + 2));
        System.out.println("3 * 4 等於 " + (3 * 4));

        int num1 = 2;
        int num2 = 3;
        int sum = num1 + num2;

        System.out.println("變數 num1 的值是 " + num1);
        System.out.println("變數 num2 的值是 " + num2);
        System.out.println("num1 + num2 的值為 " + sum);

        num1++;
        System.out.println("變數 num1 加 1 之後是 " + num1);
    }
}
